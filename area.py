import math

def RectangleArea(_base, _hauteur):
    sum=_base*_hauteur
    return sum

def CircleArea(_ray):
    sum=math.pi*_ray*_ray
    return sum

def SquareArea(_base, _hauteur):
    sum=_base*_hauteur
    return sum

def TriangleArea(_base, _hauteur):
    sum=(_base*_hauteur)/2
    return sum